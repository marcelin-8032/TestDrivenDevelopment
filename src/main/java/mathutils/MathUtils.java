package mathutils;

import static java.lang.Math.PI;

public class MathUtils {
    public int add(int i, int i1) {
        return i + i1;
    }

    public double computeCircleArea(double i) {
        return PI * i * i;
    }

    public double computeCirclePerimetre(double j) {
        return 2*PI*j;
    }
}
