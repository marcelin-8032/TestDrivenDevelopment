package SpringBoottdd;


public class Car {
    private String type;
    private String name;

    public Car(String name, String type) {
        this.name = name;
        this.type = type;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

}
